      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='shenqingbianhao'){
              this.ruleForm.shenqingbianhao = obj[o];
              this.ro.shenqingbianhao = true;
              continue;
            }
            if(o=='gangweimingcheng'){
              this.ruleForm.gangweimingcheng = obj[o];
              this.ro.gangweimingcheng = true;
